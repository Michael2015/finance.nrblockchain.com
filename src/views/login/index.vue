      <div class="login-wrapper">
        <div class="title-container">
          <h3 class="title">商家管理后台</h3>
        </div>
        <el-tabs v-model="activeType" @tab-click="changeType">
          <el-tab-pane label="密码登录" name="password">
            <el-form-item prop="username">
